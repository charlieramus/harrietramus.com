// -----------------------------------------------------------------------------
// THEME  (the vintage-postcard palette)
// -----------------------------------------------------------------------------
// The colour system, as data. lib/theme.ts turns this object into the per-mode
// CSS custom properties in one place (themeToCss) — so there are no hand-copied
// hexes in the stylesheet. Two modes: `dark` (deep-ink ground, default) and
// `light` (warm-cream ground). The accent *trio* (tomato / teal / mustard) is
// what makes each collection its own colour — see `accent` on each collection.

/** The three accent hues. Each collection picks one of these keys. */
export interface Trio {
  tomato: string;
  teal: string;
  mustard: string;
}

/** The non-accent "grounds" for one mode — everything that isn't the trio. */
export interface Grounds {
  /** page background */
  bg: string;
  /** raised surface (cards, plates) */
  bg2: string;
  /** primary text */
  ink: string;
  /** secondary text */
  inkSoft: string;
  /** faint text / hairline labels */
  inkFaint: string;
  /** text/icon colour that sits *on* an accent fill */
  accentInk: string;
  /** hairline / divider colour */
  line: string;
  /** translucent sticky-nav background */
  navBg: string;
  /** the lightswitch wall-plate colour */
  plate: string;
}

export interface Theme {
  /** which trio is live. "postcard" is the shipped one. */
  active: "postcard";
  palettes: {
    /** the bright trio used in dark mode */
    postcard: Trio;
    /** deepened trio variants, used in light mode for legibility */
    accentLight: Trio;
    /** alternative single-accent presets — not live, kept for quick reskins */
    presets: Record<string, string>;
  };
  /** which trio hue is the default `--accent` when nothing overrides it */
  defaultAccent: keyof Trio;
  dark: Grounds;
  light: Grounds;
}

