// -----------------------------------------------------------------------------
// COLLECTIONS  (one entry per trip)
// -----------------------------------------------------------------------------
// The collection wall. Each collection is a full-bleed hero card over a stack of
// thin Place cards, and the whole thing is tinted by ONE trio hue via `accent`
// (Africa → mustard, United States → teal, Japan → tomato). `essay` is the slug
// that links a collection/place into /journal/[essay] (built in V3). `hero` /
// `cls` are .ph-* gradient stand-ins until real photos land in V4.

/** One place inside a collection — a thin stacked card. */
export interface Place {
  /** place name (serif) */
  name: string;
  /** .ph-* gradient stand-in class for this place's photo */
  cls: string;
  /** right-aligned meta label (region / country) */
  meta: string;
  /** the `#code` of the synced photo (data/photos.ts) to show behind this card.
   *  When it resolves, a real next/image renders; otherwise `cls` stands in. */
  code?: string;
}

/** One trip. */
export interface Collection {
  /** collection name (serif title) */
  name: string;
  /** .ph-* gradient stand-in class for the hero photo */
  hero: string;
  /** the stat line; the leading number renders in the mono face + accent */
  stat: string;
  /** slug → /journal/[essay], and the key into `essays` below */
  essay: string;
  /** which trio hue tints this collection */
  accent: keyof Trio;
  /** the `#code` of the synced photo (data/photos.ts) to show behind the hero.
   *  When it resolves, a real next/image renders; otherwise `hero` stands in. */
  code?: string;
  places: Place[];
}

